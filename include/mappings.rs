pub fn futhark_to_latin(rune: char) -> char {
    match rune {
        'ᚠ' => 'f',
        'ᚢ' => 'u',
        'ᚦ' => 'þ',
        'ᚨ' => 'a',
        'ᚱ' => 'r',
        'ᚲ' => 'k',
        'ᚷ' => 'g',
        'ᚹ' => 'w',
        'ᚺ' => 'h',
        'ᚻ' => 'h',
        'ᚾ' => 'n',
        'ᛁ' => 'i',
        'ᛃ' => 'j',
        'ᛇ' => 'æ',
        'ᛈ' => 'p',
        'ᛉ' => 'z',
        'ᛊ' => 's',
        'ᛋ' => 's',
        'ᛏ' => 't',
        'ᛒ' => 'b',
        'ᛖ' => 'e',
        'ᛗ' => 'm',
        'ᛚ' => 'l',
        'ᛜ' => 'ŋ',
        'ᛟ' => 'o',
        'ᛞ' => 'd',
        '-' => ' ',

        rune => rune,
    }
}

